public class CWH_17_LOGICALoperator {
    public static void main(String[] args) {
        boolean b = false;
        boolean a = true;
        if (a && b) {
            System.out.println("Y");
        }
        else {
            System.out.println("N");
        }
        if (a || b) {
            System.out.println("Y");
        }
        else {
            System.out.println("N");
        }

    }
}
