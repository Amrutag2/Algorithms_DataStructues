public class CWH_21_CH5_Loops {
    public static void main(String[] args) {
        //loops in java 3types 1) while 2)do-while 3)for loop

        //1) while loop
        int i = 1;
        while(i<=3){
            System.out.println(i);
            i++;
        }
        System.out.println("finish while loop");
        //while(1) not allowed in java only while(true) is allowed

    }
}
